// for loop
console.log("for loop");
for (i = 10; i >= 0; i -= 2) {
  console.log(i);
  // if (i == 0) {
  //   break;
  // }
} //break to quit a loop, iteration

// while loop
console.log("while loop");
let x = 1;
while (x < 1) {
  console.log(x++);
}

// do while
console.log("do while loop");
let y = 1;
do {
  console.log(y);
  y++;
} while (y < 1);
// execute something before checking the condition
